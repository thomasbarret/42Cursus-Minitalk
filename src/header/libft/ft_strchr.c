#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == c % 128)
			return ((char *)&s[i]);
		i++;
	}
	if (c % 128 == 0)
		return ((char *)&s[i]);
	return (0);
}
